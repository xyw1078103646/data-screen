<template>
  <div class="box">
    <el-scrollbar class="h-100">
      <div class="itemBox">
        <div
          class="item sc-flex sc-ai-center sc-cursor"
          v-for="item in list"
          :key="item.id"
          @click="goDetail(item)"
        >
          <svg-icon icon-class="records"></svg-icon>
          <div class="sc-fs-ellipsis sc-text-white">{{ item.stateName }}</div>
          <div class="sc-text-white sc-mr-2">{{ item.showTime }}</div>
          <div class="sc-text-white">{{ item.typeName }}</div>
          <template v-if="item.handle == 1">
            <div class="tag sc-flex sc-ai-center type1">误报</div>
            <div class="tag sc-flex sc-ai-center type2">未处理</div>
            <div class="tag sc-flex sc-ai-center type3">需救援</div>
          </template>
          <template v-else>
            <div class="sc-text-white sc-ml-2">已处理</div>
          </template>
        </div>
      </div>
    </el-scrollbar>
    <!-- 报警详情 -->
    <el-dialog
      title="报警详情"
      :visible.sync="detailFlag"
      :fullscreen="true"
      :before-close="closeDialog"
    >
      <template v-if="detailData">
        <el-descriptions title="设备信息">
          <el-descriptions-item label="安装位置">{{
            detailData.address
          }}</el-descriptions-item>
          <el-descriptions-item label="设备类型">{{
            detailData.kindName
          }}</el-descriptions-item>
        </el-descriptions>
        <el-descriptions title="报警信息" class="sc-mt-3">
          <el-descriptions-item label="设备状态">{{
            detailData.status
          }}</el-descriptions-item>
          <el-descriptions-item label="报警明细">{{
            detailData.address
          }}</el-descriptions-item>
          <el-descriptions-item label="报警时间">{{
            detailData.address
          }}</el-descriptions-item>
          <el-descriptions-item label="通知次数">{{
            detailData.address
          }}</el-descriptions-item>
        </el-descriptions>
        <el-descriptions title="处理信息" class="sc-mt-3">
          <el-descriptions-item label="处理方式">{{
            detailData.address
          }}</el-descriptions-item>
          <el-descriptions-item label="处理时间">{{
            detailData.address
          }}</el-descriptions-item>
          <el-descriptions-item label="处理照片">{{
            detailData.address
          }}</el-descriptions-item>
        </el-descriptions>
      </template>
    </el-dialog>
  </div>
</template>

<script>
import { getDetail } from "@/api/operationApi.js";
export default {
  props: {
    list: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data() {
    return {
      detailFlag: false,
      detailData: null,
    };
  },
  methods: {
    //打开详情弹框
    async goDetail(row) {
      const res = await getDetail({
        handle: row.handle,
        state: row.state,
        deviceId: row.id,
        timestamp: row.timestamp,
      });
      this.detailData = res.data;
      this.detailFlag = true;
    },
    //关闭弹框
    closeDialog() {
      this.detailData = null;
      this.detailFlag = false;
    },
  },
};
</script>

